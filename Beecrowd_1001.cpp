#include <iostream> // Libs entrada/saída em C++
#include <cstdio>
using namespace std;


// Tipo nome conjunto de parâmetros () {} Assinatura da função
int main(){
    int x, y;
    cin >> x;
    cin >> y;
    cout << "X = " << x + y << endl;    

    cout << "X = " << x - y << endl;    

    cout << "X = " << x * y << endl; 
    
    cout << "X = " << x / y << endl;    

    return 0;
}
